const fs = require('fs');
let content = fs.readFileSync('src/components/tech-stack-builder.tsx', 'utf8');

const replacements = [
  // 1. Remove all glassmorphism
  { pattern: /backdrop-blur-[a-z]+/g, replacement: '' },
  
  // 2. Fix Card borders and shadows (Anthropic flat design)
  { pattern: /shadow-sm border border-border\/50/g, replacement: 'border border-border shadow-xs' },
  { pattern: /shadow-md/g, replacement: 'shadow-sm' },

  // 3. Remove Gradients (Anthropic strictly avoids gradients, preferring solid primary)
  { pattern: /bg-gradient-to-r from-primary to-secondary/g, replacement: 'bg-primary' },
  { pattern: /bg-gradient-to-br from-blue-600 to-purple-600/g, replacement: 'bg-primary' },
  { pattern: /hover:from-primary\/90 hover:to-secondary\/90/g, replacement: 'hover:bg-primary/90' },
  { pattern: /from-slate-50 to-slate-100/g, replacement: 'bg-background' },
  { pattern: /from-slate-900 to-slate-800/g, replacement: 'bg-background' },

  // 4. Fix specific UI container classes
  { pattern: /bg-background border-r border-border/g, replacement: 'bg-card border-r border-border' },
  { pattern: /bg-card border-r border-border/g, replacement: 'bg-card border-r border-border' }, // keep this
  { pattern: /bg-background text-foreground flex flex-col lg:flex-row/g, replacement: 'bg-background text-foreground flex flex-col lg:flex-row' },
  
  // 5. Typography sizing fixes
  { pattern: /text-\[11px\]/g, replacement: 'text-xs' },
  { pattern: /font-display tracking-tight/g, replacement: 'font-display tracking-tight font-semibold' },
  
  // 6. Action buttons cleanup
  { pattern: /bg-background border-border !text-foreground hover:bg-accent text-accent-foreground hover:!text-foreground/g, replacement: 'bg-card border-border text-foreground hover:bg-accent hover:text-accent-foreground transition-colors' },

  // 7. Icon Colors
  { pattern: /text-purple-400/g, replacement: 'text-primary' },
  { pattern: /text-purple-600/g, replacement: 'text-primary' },

  // 8. Fix spacing
  { pattern: /p-4 sm:p-4 lg:p-6/g, replacement: 'p-6 lg:p-8' }
];

for (const {pattern, replacement} of replacements) {
    content = content.replace(pattern, replacement);
}

// Clean up any double spaces generated by regex removals
content = content.replace(/  +/g, ' ');
content = content.replace(/className=\" /g, 'className=\"');

fs.writeFileSync('src/components/tech-stack-builder.tsx', content);
console.log('Anthropic Restyling Script updated successfully');
